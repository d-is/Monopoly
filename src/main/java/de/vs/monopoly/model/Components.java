package de.vs.monopoly.model;

public class Components {
	public String game;
	public String dice; 
	public String board;
	public String bank;
	public String broker;
	public String decks;
	public String events;
	
	public Components(String game, String dice, String board, String bank, String broker, String decks, String events){
		this.game = game;
		this.dice = dice;
		this.board = board;
		this.bank = bank;
		this.broker = broker;
		this.decks = decks;
		this.events = events;
	}
}
